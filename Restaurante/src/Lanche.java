
public class Lanche extends Item {
	
	public String igrediente;
	public int peso;
	
public Lanche() {
		
	}
	public String getIgrediente() {
		return igrediente;
	}
	public void setIgrediente(String igrediente) {
		this.igrediente = igrediente;
	}
	public int getPeso() {
		return peso;
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}
	
	

}
